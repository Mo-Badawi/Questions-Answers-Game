// بناء تصنيف "الكل" من كل الفئات
for (const key in categories) {
  if (key !== "الكل") categories["الكل"] = categories["الكل"].concat(categories[key]);
}

let currentCategory = "الكل";
let asked = [];

function renderCategories() {
  const container = document.getElementById('categories');
  container.innerHTML = '';
  Object.keys(categories).forEach(cat => {
    const btn = document.createElement('button');
    btn.className = 'cat-btn' + (cat === currentCategory ? ' active' : '');
    btn.textContent = cat;
    btn.onclick = () => selectCategory(cat);
    container.appendChild(btn);
  });
}

function selectCategory(cat) {
  currentCategory = cat;
  asked = [];
  renderCategories();
  document.getElementById('questionText').textContent = 'اكبس "سؤال جديد" عشان تبدأ 👇';
  document.getElementById('counter').textContent = '';
  document.getElementById('currentCat').textContent = 'التصنيف: ' + cat;
}

function newQuestion() {
  const pool = categories[currentCategory];
  if (asked.length === pool.length) asked = [];
  let idx;
  do {
    idx = Math.floor(Math.random() * pool.length);
  } while (asked.includes(idx));
  asked.push(idx);
  document.getElementById('questionText').textContent = pool[idx];
  document.getElementById('counter').textContent = `سؤال ${asked.length} من ${pool.length}`;
}

renderCategories();
document.getElementById('currentCat').textContent = 'التصنيف: الكل';
